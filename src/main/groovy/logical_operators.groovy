// Логические операторы: https://stepik.org/lesson/686402/step/3?unit=685497

/*
Groovy предлагает три логических оператора для логических выражений:

&&: логическое "и"
||: логическое "или"
!: логическое "не"
 */

println(!false)
println(true && true)
println(false && true)
println(true || false)
println(false || true)
